const int N = 2e5 + 5;

void solve () {
  int t; cin >> t;
  while (t--) {
    int n; cin >> n;
    int maximum = 0, minimum = 1000;
    for (int i = 0; i < n; i++) {
      int x; cin >> x;
      maximum = max(maximum, x);
      minimum = min(minimum, x);
    }
    cout << (maximum - minimum) * (n - 1) << endl;
  }
}

